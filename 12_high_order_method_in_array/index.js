
/// high order method
//map method
let ara=[1,2,3,4,5]
let doubled=ara.map((el)=>el*9);
console.log(doubled)

//filter method

let araa=[1,2,3,4,5,6,7,8,9]
let even=araa.filter((e)=>e%2===0)
console.log(even)

//reduce
let araaa=[1,2,3,4,5]
let sum=araaa.reduce((prev,el)=>prev+el,8)
console.log(sum)